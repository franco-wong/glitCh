package com.redrabbit.helpers;

/**
 * A Wrapper class for moveable objectts
 * 
 * @author rabbitfighter
 *
 */
public class Moveable
{
	private float x, y;

	public float getX()
	{
		return x;
	}

	public void setX(float x)
	{
		this.x = x;
	}

	public float getY()
	{
		return y;
	}

	public void setY(float y)
	{
		this.y = y;
	}

}
